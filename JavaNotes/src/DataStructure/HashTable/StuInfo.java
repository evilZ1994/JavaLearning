package DataStructure.HashTable;

public class StuInfo {
    private int age;
    private int number;

    public StuInfo(int age, int number) {
        this.age = age;
        this.number = number;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
